function addPiece() {
  const aNewPiece = new Piece(
    random(windowWidth),
    random(windowHeight),
    random(emoji),
    random(windowHeight)
  );
  myPersonalPieces.push(aNewPiece);
}

class Piece {
  constructor(temp_x, temp_y, temp_rand, temp_t) {
    this.x = temp_x;
    this.y = temp_y;
    this.rand = temp_rand;
    this.t = temp_t;
  }

  display() {
    text(this.rand, this.x, this.y);
    textSize(40);
  }

  updatePosition() {
    this.t += 0.003;
    this.x = width * noise(this.t);
    this.y = height * noise(this.t + 5);
  }

  run() {
    this.updatePosition();
    this.display();
  }
}
function windowResized() {
  resizeCanvas(windowWidth, windowHeight);
}

function backgroundImage(img) {
  push();
  translate(width / 2, height / 2);
  imageMode(CENTER);
  let scale = Math.max(width / img.width, height / img.height);
  image(img, 0, 0, img.width * scale, img.height * scale);
  pop();
}

